    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Sms  $sms
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $messages = Sms::findOrFail($id);
        $messages->delete();
        return redirect()->route('message.index');

    }
